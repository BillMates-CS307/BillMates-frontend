export function printLogHeading(funcName, status) {
    let output;
    switch(status) {
        case 200:
            output = `\x1b[32m============================================${funcName}============================================\x1b[0m`;
            break;
        case 400:
            output = `\x1b[33m============================================${funcName}============================================\x1b[0m`;
            break;
        case 403:
            output = `\x1b[36m============================================${funcName}============================================\x1b[0m`;
            break;
        case 404:
            output = `\x1b[33m============================================${funcName}============================================\x1b[0m`;
            break;
        case 500:
            output = `\x1b[31m============================================${funcName}============================================\x1b[0m`;
            break;
        case 502:
            output = `\x1b[35m============================================${funcName}============================================\x1b[0m`;
            break;
        default:
            output = `\x1b[31m============================================${funcName}============================================\nStatus Code:${status}\x1b[0m`;
    }
    console.log(output);
}

